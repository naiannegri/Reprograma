function diaSemana(dia){
    if (dia == 1) {
        return "Domingo"
    }
    else if (dia == 2) {
        return "Segunda"
    }
    else if (dia == 3){
        return "Terça"
    }
    else if (dia == 4){
        return "Quarta"
    }
    else if (dia == 5){
        return "Quinta"
    }
    else if (dia == 6){
        return "Sexta"
    }
    else if (dia == 7){
        return "Sábado"
    }
}

console.log(diaSemana(5))